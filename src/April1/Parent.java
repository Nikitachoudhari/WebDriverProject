package April1;

public class Parent {
	public static int a , b , c;
	
	public static void add() {
		a = 10 ; 
		b = 11 ;
		c = a + b ;
		System.out.println(c);
	}
	
	public static void sub() {
		a = 10 ; 
		b = 11 ;
		c = a - b ;
		System.out.println(c);
	}
	
	public static void div() {
		a = 10 ; 
		b = 5 ;
		c = a / b ;
		System.out.println(c);
	}
	
	
}
